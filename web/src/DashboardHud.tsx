interface StatusRingProps {
  kind: DashboardStatusKind;
  label: string;
  value: number;
}

export function StatusRing({ kind, label, value }: StatusRingProps) {
  const radius = 23.5;
  const circumference = 2 * Math.PI * radius;
  const boundedValue = Math.max(0, Math.min(100, value));
  const offset = circumference * (1 - boundedValue / 100);
  const tone = value < 30 ? "#df5964" : value >= 70 ? "#3fff8f" : "#ffb107";
  return (
    <div
      className="stat-ring"
      data-status-kind={kind}
      aria-label={`${label}: ${value}`}
      style={{ "--stat-tone": tone } as CSSProperties}
    >
      <svg viewBox="0 0 54 54" aria-hidden="true">
        <circle className="stat-ring__track" cx="27" cy="27" r={radius} />
        <circle
          className="stat-ring__value"
          cx="27"
          cy="27"
          r={radius}
          strokeDasharray={circumference}
          strokeDashoffset={offset}
        />
        <path className="stat-ring__glyph" d={DASHBOARD_STATUS_GLYPH_PATHS[kind]} />
      </svg>
    </div>
  );
}

